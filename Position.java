public class Position {
	
	private int x;
	private int y;
	
	public Position(int x, int y){
		this.x = x;
		this.y = y;
	}
	
	/*
	 * @return x
	 */
	public int getX(){
		return x;
	}
	
	/*
	 * @return y
	 */
	public int getY(){
		return y;
	}

}
